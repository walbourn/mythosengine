// Works like process(), but yields the current task until an event in      
// the event mask becomes active.                                           
//
MaxEventList MaxEventSource::process_wait (const MaxEventList &mask)
{
    MaxEventList    res (0);

    do
    {
        bz_suspend();
        res = process (mask);
    }
    while (res.empty());

    return res;
}


