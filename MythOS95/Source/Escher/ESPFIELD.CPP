// EschParticleField - release                                              ³
//                                                                          ³
// Releases all data associated with the particle field.                    ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
void EschParticleField::release(void)
{
    if (flags & ESCH_DRW_OWNSDATA)
    {
        if (hparticles)
            ivory_hfree(&hparticles);

        flags &= ~ESCH_DRW_OWNSDATA;
    }

    hparticles=0;
    count=0;
}


//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ¿
// EschParticleField - draw                                                 ³
//                                                                          ³
// Draws the field of particles, if any.                                    ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
void EschParticleField::draw(void)
{
    ulong           i;
    EschParticle    *ptr;

    if (!count || !hparticles)
        return;

    ptr = (EschParticle *)ivory_hlock(hparticles);

    assertMyth("EschParticleField::draw can't lock particle array",
               ptr);

    if (!ptr)
        return;

    for(i=0; i < count; i++, ptr++)
    {
        if (!(ptr->flags & ESCH_DRW_SKIP))
        {
            ptr->draw();
        }
    }

    ivory_hunlock(hparticles);
}


