    EschBoxExtents(float minx, float miny, float minz,
                   float maxx, float maxy, float maxz)
        { mins[0] = minx; mins[1] = miny; mins[2] = minz;
          maxs[0] = maxx; maxs[1] = maxy; maxs[2] = maxz; }
