// EschMultiFrameTexture - release                                          ³
//                                                                          ³
// Removes the texture data from memory.                                    ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
void EschMultiFrameTexture::release()
{
    if (flags & ESCH_TXT_OWNSDATA)
    {
        if (handle)
            ivory_hfree(&handle);
    }

    EschTexture::release();
}


