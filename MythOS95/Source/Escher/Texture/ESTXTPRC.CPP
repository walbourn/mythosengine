// EschProceduralTexture - release                                          ³
//                                                                          ³
// Removes the output buffer from memory.                                   ³
//ÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÄÙ
void EschProceduralTexture::release()
{
    if (txt.tex)
    {
        delete [] txt.tex;
        txt.tex = 0;
    }

    EschTexture::release();
}


