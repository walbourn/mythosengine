void ClientUserCode(EXPbuf *buf)
{
   switch(buf->usercode)
   {
       case 0x0200:
           do_user_interaction();
           buf->opcode=EXP_TERMINATE;
           buf->usercode=EXP_TERMINATE;
           break;
       default:
           buf->opcode=buf->usercode=EXP_TERMINATE;
           buf->status=0;
           break;
   }
}

