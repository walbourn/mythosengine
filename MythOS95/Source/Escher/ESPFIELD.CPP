// EschParticleField - nextframe                                            ³
//                                                                          ³
// Updates each particle object for next frame, if any.                     ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
void EschParticleField::nextframe(void)
{
    ulong           i;
    EschParticle    *ptr;

    if (!count || !hparticles)
        return;

    ptr = (EschParticle *)ivory_hlock(hparticles);

    assertMyth("EschParticleField::nextframe can't lock particle array",
               ptr);

    if (!ptr)
        return;

    for(i=0; i < count; i++, ptr++)
    {
        ptr->nextframe();
    }

    ivory_hunlock(hparticles);
}



