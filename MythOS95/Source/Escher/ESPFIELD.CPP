// EschParticleField - set_flags                                            ³
//                                                                          ³
// Turns on or off flags in all particles within field.                     ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
void EschParticleField::set_flags(dword mask, byte value)
{
    ulong           i;
    EschParticle    *ptr;
    
    if (!count || !hparticles)
        return;

    ptr = (EschParticle *)ivory_hlock(hparticles);

    assertMyth("EschParticleField::set_flags can't lock particle array",
               ptr);

    if (!ptr)
        return;

    for(i=0; i < count; i++, ptr++)
    {
        if (value)
            ptr->flags |= mask;
        else
            ptr->flags &= ~mask;
    }

    ivory_hunlock(hparticles);
}

