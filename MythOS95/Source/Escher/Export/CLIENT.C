void ClientStartup(EXPbuf *buf)
{
   int version=studio_version();
   
   if(version<400)
   {
       strcpy(buf->data.string, "ERROR: Requires 3DS version 4.0 or greater");
       buf->opcode=EXP_CONTINUE;
       buf->usercode=EXP_TERMINATE;
   }
   else
   {
       buf->opcode=EXP_NOP;
       buf->usercode=0x0200;
   }
}

