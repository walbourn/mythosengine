// EschProceduralTexture - init                                             ³
//                                                                          ³
// Sets up the buffers and performs other initialization for use.           ³
//                                                                          ³
// The base implementation just allocates the output buffer for generate()  ³
// to use.                                                                  ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
esch_error_codes EschProceduralTexture::init(ushort w, ushort h,
                                             VngoPal *pal, int dogen)
{
//ִִִ Delete any previous buffer
    if (txt.tex)
        delete [] txt.tex;

//ִִִ Verify valid width and height
    switch (w)
    {
        case 16:
        case 32:
        case 64:
        case 128:
        case 256:
            break;
        default:
            return ESCH_ERR_INVALIDPARMS;
    }

    switch (h)
    {
        case 16:
        case 32:
        case 64:
        case 128:
        case 256:
            break;
        default:
            return ESCH_ERR_INVALIDPARMS;
    }

//ִִִ Initialize output buffer
    txt.width = w;
    txt.height = h;

    txt.tex = new byte[txt.width * txt.height];
    if (!txt.tex)
        return ESCH_ERR_NOMEMORY;

//ִִִ Do generate, if requested
    if (dogen)
        generate();

    return ESCH_ERR_NONE;
}


