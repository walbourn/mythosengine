// EschParticleField - Constructor                                          ³
//                                                                          ³
// Initializes the particle field's particle array and count.               ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
EschParticleField::EschParticleField(ulong c, IvoryHandle hps, int owns)
{
    count=c;
    hparticles=hps;

    if (owns)
        flags |= ESCH_DRW_OWNSDATA;
}


//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ¿
// EschParticleField - Destructor                                           ³
//                                                                          ³
// Frees particles, if owned by the drawable.                               ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
EschParticleField::~EschParticleField()
{
    release();
};



