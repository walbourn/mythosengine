// EschMultiFrameTexture - animate                                          ³
//                                                                          ³
// Advances the texture to the next frame in the sequence.                  ³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
void EschMultiFrameTexture::animate()
{
    index++;
    index %= max;
}

