//Ŀ
// BachSystem - Constructor                                                 
//

BachSystem::BachSystem()
{
// Safeguard 
     if (BachSysInstance)
          return;

     BachSysInstance=this;

// Initialize instance information
     flags=0;
};


//Ŀ
// BachSystem - Destructor                                                  
//                                                                          
// Warning:  For AIL, the timer system is also released by this destructor! 
//
BachSystem::~BachSystem()
{
// Safeguard
     if (!BachSysInstance)
          return;

     BachSysInstance=0;
};

