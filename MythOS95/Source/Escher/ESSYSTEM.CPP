// EschSystem - Constructor                                                 ³
//                                                                          ³
// Sets up the library, initializing the library if the arena_size is given.³
//ִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִִ
EschSystem::EschSystem(ulong arena_size) :
    wspace_mbytes(0),
    wspace_sbytes(0),
    wspace(0),
    diag_faces(0),
    diag_verts(0),
    diag_ifaces(0),
    diag_iverts(0),
    diag_ntxts(0),
    tcache_hi(0),
    tcache_max(0),
    tcache(0)
{
    esch_error_codes    err;

    assertMyth("EschSystem: Only one instance is allowed.",
               !EschSysInstance);

    EschSysInstance=this;

    //ִִ Create workspace, if arena_size given
    if (arena_size)
    {
        err=init(arena_size);
        assert (err == ESCH_ERR_NONE);
    }
}


